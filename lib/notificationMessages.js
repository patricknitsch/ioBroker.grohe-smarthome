/**
 * Returns a localized notification message for the given key, substituting any placeholders.
 *
 * Falls back to English when the adapter's configured system language is not supported.
 *
 * @param {object} adapter                          - The ioBroker adapter instance.
 * @param {string} key                              - Message key (e.g. 'valveOpened').
 * @param {Record<string, string|number>} [params]  - Placeholder values keyed by token name.
 * @returns {string} Localized message with placeholders replaced.
 */
function getNotificationMessage(adapter, key, params) {
	const lang = adapter.systemLanguage || 'en';
	const langMsgs = messages[lang] || messages.en;
	const template = langMsgs[key] || messages.en[key] || key;
	if (!params) {
		return template;
	}
	return template.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? String(params[k]) : `{${k}}`));
}

/**
 * Returns the localized notification type text for a given Grohe API notification (category + type).
 *
 * Falls back to English when the language is not supported.
 *
 * @param {object} adapter - The ioBroker adapter instance.
 * @param {number} category - Notification category (e.g. 20 for Warning, 30 for Alarm).
 * @param {number} type     - Notification type number.
 * @returns {string} Localized notification type description.
 */
function getLocalizedNotificationType(adapter, category, type) {
	const lang = adapter.systemLanguage || 'en';
	const key = `${category}_${type}`;
	const langTypes = notificationTypes[lang] || notificationTypes.en;
	return langTypes[key] || notificationTypes.en[key] || `Unknown (${key})`;
}

module.exports = { getNotificationMessage, getLocalizedNotificationType };
